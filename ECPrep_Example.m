fold = 'J:\Airport_Wetlands_2015\Data\EC Towers';
saveFold = 'J:\Airport_Wetlands_2015\Data\Scintillometer';
sites = {'Wet', 'Middle', 'Dry'};
for ii=1:3
    ECAirport.(sites{ii}) = getUTESData(fold, datenum(2015, 09, 23), datenum(2015, 12, 12));
end


ECData.T_FW = nanmean([...
    ECAirport.(sites{1}).Int_R_20Hz(1:3840, 2), ...
    ECAirport.(sites{2}).Mid_20Hz(1:3840, 2),...
    ECAirport.(sites{3}).Int_T_20Hz(1:3840, 2),...
    ], 2)+273.15;

ECData.T_Son = nanmean([...
    ECAirport.(sites{1}).Int_R_20Hz(1:3840, 6), ...
    ECAirport.(sites{2}).Mid_20Hz(1:3840, 6),...
    ECAirport.(sites{3}).Int_T_20Hz(1:3840, 6),...
    ], 2)+273.15;

ECData.P = nanmean([...
    ECAirport.(sites{1}).Int_R_20Hz(1:3840, 12), ...
    ECAirport.(sites{2}).Mid_20Hz(1:3840, 12),...
    ECAirport.(sites{3}).Int_T_20Hz(1:3840, 12),...
    ], 2).*1000;

ECData.Q = nanmean([...
    ECAirport.(sites{1}).Int_R_20Hz(1:3840, 9), ...
    ECAirport.(sites{2}).Mid_20Hz(1:3840, 9),...
    ECAirport.(sites{3}).Int_T_20Hz(1:3840, 9),...
    ], 2);

ECData.rho = nanmean([...
    ECAirport.(sites{1}).H(1:3840, 2), ...
    ECAirport.(sites{2}).H(1:3840, 2),...
    ECAirport.(sites{3}).H(1:3840, 2),...
    ], 2);

ECData.Cp = nanmean([...
    ECAirport.(sites{1}).H(1:3840, 3), ...
    ECAirport.(sites{2}).H(1:3840, 3),...
    ECAirport.(sites{3}).H(1:3840, 3),...
    ], 2);

ECData.Lv = nanmean([...
    ECAirport.(sites{1}).LHflux(1:3840, 2), ...
    ECAirport.(sites{2}).LHflux(1:3840, 2),...
    ECAirport.(sites{3}).LHflux(1:3840, 2),...
    ], 2);

ECData.uStar = nanmean([...
    sqrt(abs(ECAirport.(sites{1}).tau(1:3840, 3)./ECAirport.(sites{1}).H(1:3840, 2))), ...
    sqrt(abs(ECAirport.(sites{2}).tau(1:3840, 3)./ECAirport.(sites{2}).H(1:3840, 2))),...
    sqrt(abs(ECAirport.(sites{3}).tau(1:3840, 3)./ECAirport.(sites{3}).H(1:3840, 2))),...
    ], 2);

ECData.Time = ECAirport.(sites{1}).H(1:3840, 1);

if ~exist(saveFold, 'dir')
    mkdir(saveFold);
end

save([saveFold, filesep, 'ECData.mat'], 'ECData')